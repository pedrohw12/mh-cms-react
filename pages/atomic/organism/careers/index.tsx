import type { NextPage } from "next";
import Head from "next/head";
import styles from "../../../../styles/Home.module.css";
import axios from "axios";
import { useEffect, useState } from "react";
import BasicSpeedDial from "../button";
import BasicButton from "../button";
import BasicModal from "../modal";
import CareerCard from "../career-card";

type Career = {
  _id: string;
  title: string;
  period: string;
  location: string;
  salary: string;
  responsibilities: string;
  requirements: string;
  niceToHave: string;
};

const Careers: NextPage = () => {
  const [careers, setCareers] = useState([]);
  const [title, setTitle] = useState("");
  const [period, setPeriod] = useState("");
  const [location, setLocation] = useState("");
  const [salary, setSalary] = useState("");
  const [responsibilities, setResponsibilities] = useState("");
  const [requirements, setRequiriments] = useState("");
  const [niceToHave, setNiceToHave] = useState("");
  const [isEditing, setIsEditing] = useState(false);
  const [isModalOpen, setIsModalOpen] = useState(false);
  const [selectedMemberToEdit, setSelectedCareerToEdit] = useState<Career>(
    {} as Career
  );

  useEffect(() => {
    axios
      .get("http://localhost:3333/careers")
      .then((response) => {
        console.log(response);
        setCareers(response.data);
      })
      .catch((err) => console.log(err));
  }, []);

  function registerCareer() {
    if (!title || !period || !location || !salary || !responsibilities) {
      alert("Please fill all the inputs.");
      return;
    }
    axios
      .post("http://localhost:3333/careers", {
        title,
        period,
        location,
        salary,
        responsibilities,
        requirements,
        niceToHave,
      })
      .then((response) => {
        console.log(response);
        alert("Career successfully registered!");
        setIsModalOpen(false);
      })
      .catch((err) => console.log(err));
  }

  function handleSubmit() {
    if (!isEditing) {
      registerCareer();
      return;
    }

    axios
      .put(`http://localhost:3333/careers/${selectedMemberToEdit._id}`, {
        title,
        period,
        location,
        salary,
        responsibilities,
        requirements,
        niceToHave,
      })
      .then((response) => {
        console.log(response);
        alert("Career successfully updated!");
        setIsModalOpen(false);
      })
      .catch((err) => console.log(err));
  }

  function handleEdit(career: Career) {
    setIsEditing(true);
    setSelectedCareerToEdit(career);
    setTitle(career.title);
    setPeriod(career.period);
    setLocation(career.location);
    setSalary(career.salary);
    setResponsibilities(career.responsibilities);
    setRequiriments(career.requirements);
    setNiceToHave(career.niceToHave);
  }

  function clearInputs() {
    setIsEditing(false);
    setTitle("");
    setPeriod("");
    setLocation("");
    setSalary("");
    setResponsibilities("");
    setRequiriments("");
    setNiceToHave("");
  }

  function handleDelete(career: Career) {
    let confirmAction = confirm("Are you sure to delete this career?");
    if (confirmAction) {
      axios
        .delete(`http://localhost:3333/careers/${career._id}`)
        .then((response) => {
          console.log(response);
          alert("Member successfully registered!");
        })
        .catch((err) => console.log(err));
      return;
    } else {
      return;
    }
  }

  return (
    <div className={styles.container}>
      <Head>
        <title>MH CMS | Career</title>
        <meta name="salary" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <BasicModal
        modalTitle="ADD"
        handleClose={() => setIsModalOpen(false)}
        handleOpen={() => {
          clearInputs();
          setIsModalOpen(true);
        }}
        isModalOpen={isModalOpen}
      >
        <div style={{ display: "flex", flexDirection: "column", width: 400 }}>
          <input
            style={{
              height: 40,
              borderRadius: 10,
              marginBottom: 5,
              padding: 10,
            }}
            placeholder="Title"
            value={title}
            onChange={(e) => setTitle(e.target.value)}
          />
          <input
            style={{
              height: 40,
              borderRadius: 10,
              marginBottom: 5,
              padding: 10,
            }}
            placeholder="Period"
            value={period}
            onChange={(e) => setPeriod(e.target.value)}
          />
          <input
            style={{
              height: 40,
              borderRadius: 10,
              marginBottom: 5,
              padding: 10,
            }}
            placeholder="Location"
            value={location}
            onChange={(e) => setLocation(e.target.value)}
          />
          <input
            style={{
              height: 40,
              borderRadius: 10,
              marginBottom: 5,
              padding: 10,
            }}
            placeholder="Salary"
            value={salary}
            onChange={(e) => setSalary(e.target.value)}
          />
          <textarea
            style={{
              height: 300,
              borderRadius: 10,
              marginBottom: 5,
              padding: 10,
            }}
            placeholder="Responsibilities"
            value={responsibilities}
            onChange={(e) => setResponsibilities(e.target.value)}
          />
          <textarea
            style={{
              height: 300,
              borderRadius: 10,
              marginBottom: 5,
              padding: 10,
            }}
            placeholder="Requiriments"
            value={requirements}
            onChange={(e) => setRequiriments(e.target.value)}
          />
          <textarea
            style={{
              height: 300,
              borderRadius: 10,
              marginBottom: 5,
              padding: 10,
            }}
            placeholder="Nice to have"
            value={niceToHave}
            onChange={(e) => setNiceToHave(e.target.value)}
          />
          <button
            style={{ height: 40, borderRadius: 10, width: "100%" }}
            className="submitButton"
            type="button"
            onClick={handleSubmit}
          >
            Submit
          </button>
        </div>
      </BasicModal>
      <h1>Careers</h1>
      <main className={styles.main}>
        <div style={{ display: "flex" }}>
          {careers.map((career: any) => (
            <CareerCard
              key={career._id}
              title={career.title}
              period={career.period}
              location={career.location}
              salary={career.salary}
              responsibilities={career.responsibilities}
              requirements={career.requirements}
              niceToHave={career.niceToHave}
              handleEdit={() => {
                handleEdit(career);
                setIsModalOpen(true);
              }}
              handleDelete={() => handleDelete(career)}
            />
          ))}
        </div>
      </main>
    </div>
  );
};

export default Careers;
